package com.lwm.po;

public class Book {
	private String book_id;
	private int num;
	private String book_pic;
	private String book_name;
	private String star;
	private String comment;
	private String tuijian;
	private String fanyi;
	private String date;
	private float price;
	private float price_e;
	public String getBook_id() {
		return book_id;
	}
	public void setBook_id(String book_id) {
		this.book_id = book_id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getBook_pic() {
		return book_pic;
	}
	public void setBook_pic(String book_pic) {
		this.book_pic = book_pic;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getStar() {
		return star;
	}
	public void setStar(String star) {
		this.star = star;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getTuijian() {
		return tuijian;
	}
	public void setTuijian(String tuijian) {
		this.tuijian = tuijian;
	}
	public String getFanyi() {
		return fanyi;
	}
	public void setFanyi(String fanyi) {
		this.fanyi = fanyi;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public float getPrice_e() {
		return price_e;
	}
	public void setPrice_e(float price_e) {
		this.price_e = price_e;
	}
	
}
